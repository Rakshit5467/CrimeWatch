  export const getDistrictsForState = (state) => {
    const districts = stateDistricts[state] || [];
    // Remove specific non-district entries and duplicates before returning
    const cleanedDistricts = districts
      .filter(district => district.toUpperCase() !== 'TOTAL' && district !== 'DELHI UT TOTAL') // Remove 'TOTAL' entries
      .filter((value, index, self) => self.indexOf(value) === index); // Remove duplicates within the list
    return cleanedDistricts.sort(); // Added sort for consistency
  }; 